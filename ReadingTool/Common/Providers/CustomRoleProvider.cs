        public override string[] GetRolesForUser(string username)
        {
            var user = HttpContext.Current.User.Identity as IUserIdentity;

            if(user == null)
            {
                var userService = DependencyResolver.Current.GetService<IUserService>();
                var existing = userService.FindOneByUsername(username);
                return existing == null ? new string[] { } : existing.Roles;
            }
            else
            {
                return user.Roles;
            }
        }

